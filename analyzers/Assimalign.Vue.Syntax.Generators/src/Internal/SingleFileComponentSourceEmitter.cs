using System.Globalization;
using System.Text;

namespace Assimalign.Vue.Syntax.Generators;

/// <summary>
/// Renders a <see cref="SingleFileComponentModel"/> into the generated partial class for a <c>.viu</c>
/// component. The output is deterministic and fully statically analyzable (no reflection, no dynamic
/// code generation), so it is trimming- and WASM/NativeAOT-safe. The <c>@template</c> block's compiled
/// render function ([V01.01.05.05]) is emitted here from the model's pre-serialized render body; helper
/// names bind <b>by name</b> to the runtime render-helper surface through the file-level
/// <c>using static</c> — the generator never references the runtime assembly, and the name/signature
/// contract is documented in <c>libraries/Assimalign.Vue.Syntax.Templates/docs/DESIGN.md</c>. The merged
/// <c>@script</c> C# ([V01.01.06.03]) remains a clearly-marked seam for its own work item.
/// </summary>
internal static class SingleFileComponentSourceEmitter
{
    /// <summary>
    /// The fully qualified runtime render-helper surface the emitted render body binds against — the C#
    /// analogue of upstream's function-mode <c>const { ... } = _Vue</c> helper import. The type ships
    /// with the runtime block/vnode helpers ([V01.01.05.02] and follow-ups); the generator only ever
    /// names it.
    /// </summary>
    private const string RenderHelperSurface = "global::Assimalign.Vue.RuntimeCore.RenderHelpers";

    /// <summary>Emits the full generated source for <paramref name="model"/>.</summary>
    /// <param name="model">The scaffold to render.</param>
    /// <returns>The generated C# source text.</returns>
    public static string Emit(in SingleFileComponentModel model)
    {
        var builder = new StringBuilder();
        builder.Append("// <auto-generated/>\n#nullable enable\n\n");

        if (model.RenderBody is not null)
        {
            // The by-name binding site for every emitted helper invocation (_openBlock,
            // _createElementBlock, _toDisplayString, ...): one file-level static import, mirroring
            // upstream's aliased helper preamble.
            builder.Append("using static ").Append(RenderHelperSurface).Append(";\n\n");
        }

        var indent = 0;
        var hasNamespace = !string.IsNullOrEmpty(model.Namespace);
        if (hasNamespace)
        {
            builder.Append("namespace ").Append(model.Namespace).Append('\n');
            builder.Append("{\n");
            indent = 1;
        }

        AppendIndent(builder, indent);
        builder.Append("// Generated partial-class scaffold for the \"").Append(model.ClassName)
            .Append("\" single-file component, compiled from\n");
        AppendIndent(builder, indent);
        builder.Append("// \"").Append(model.FileName)
            .Append("\" by the Assimalign.Vue.Syntax source generator ([V01.01.06.02]).\n");
        AppendIndent(builder, indent);
        builder.Append("//\n");
        AppendIndent(builder, indent);
        builder.Append("// Parsed blocks: @template=").Append(Present(model.HasTemplate))
            .Append(", @script=").Append(Present(model.HasScript))
            .Append(", @style=").Append(Count(model.StyleCount))
            .Append(", custom=").Append(Count(model.CustomBlockCount)).Append(".\n");

        AppendIndent(builder, indent);
        builder.Append("partial class ").Append(model.ClassName).Append('\n');
        AppendIndent(builder, indent);
        builder.Append("{\n");

        var bodyIndent = indent + 1;
        if (model.RenderBody is { } renderBody)
        {
            // [V01.01.05.05] The compiled @template render function. The body was serialized by the
            // template compiler's RenderFunctionEmitter (the Vue 3.5 codegen.ts analogue) and is
            // already indented for this nesting depth.
            AppendIndent(builder, bodyIndent);
            builder.Append("/// <summary>\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("/// The number of render cache slots (<c>v-once</c> subtrees and cached handlers) the\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("/// <see cref=\"Render\"/> function uses; the runtime allocates <c>_cache</c> with this length.\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("/// </summary>\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("internal const int RenderCacheSize = ").Append(Count(model.RenderCacheSize)).Append(";\n");
            builder.Append('\n');
            AppendIndent(builder, bodyIndent);
            builder.Append("/// <summary>\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("/// The compiled render function for the <c>@template</c> block ([V01.01.05.05]) — the C#\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("/// analogue of the component's compiled <c>render</c> in Vue 3.5 (vuejs/core\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("/// packages/compiler-core/src/codegen.ts). The runtime normalizes the returned value.\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("/// </summary>\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("internal static object? Render(").Append(model.ClassName).Append(" _ctx, object?[] _cache)\n");
            AppendIndent(builder, bodyIndent);
            builder.Append("{\n");
            builder.Append(renderBody);
            AppendIndent(builder, bodyIndent);
            builder.Append("}\n");
        }
        else
        {
            AppendIndent(builder, bodyIndent);
            builder.Append("// [V01.01.05.05] No @template block: no render function is emitted for this component.\n");
        }

        builder.Append('\n');
        AppendIndent(builder, bodyIndent);
        builder.Append("// [V01.01.06.03] Script merge seam. The @script block's C# is merged into this partial class\n");
        AppendIndent(builder, bodyIndent);
        builder.Append("// (with #line mapping) once script analysis lands; the scaffold emits no @script body yet.\n");

        AppendIndent(builder, indent);
        builder.Append("}\n");

        if (hasNamespace)
        {
            builder.Append("}\n");
        }

        return builder.ToString();
    }

    private static string Present(bool present) => present ? "present" : "absent";

    private static string Count(int count) => count.ToString(CultureInfo.InvariantCulture);

    private static void AppendIndent(StringBuilder builder, int levels)
    {
        for (var level = 0; level < levels; level++)
        {
            builder.Append("    ");
        }
    }
}
